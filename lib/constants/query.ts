/**
 * Константы для конфигурации TanStack Query
 * Централизованное управление настройками кеширования и повторных попыток
 */

/**
 * Время, в течение которого данные считаются свежими (staleTime)
 * Пока данные свежие, React Query не будет делать повторные запросы
 */
export const QUERY_STALE_TIME = {
  /** Транзакции - часто меняются, короткое время свежести (2 минуты) */
  TRANSACTIONS: 2 * 60 * 1000,
  /** Категории - редко меняются, длительное время свежести (10 минут) */
  CATEGORIES: 10 * 60 * 1000,
  /** Счета - редко меняются, длительное время свежести (10 минут) */
  ACCOUNTS: 10 * 60 * 1000,
  /** Доступные месяцы и годы - редко меняются (5 минут) */
  AVAILABLE_MONTHS: 5 * 60 * 1000,
  /** Значение по умолчанию (5 минут) */
  DEFAULT: 5 * 60 * 1000,
} as const;

/**
 * Время жизни кеша после того, как данные стали stale (gcTime)
 * После этого времени данные будут удалены из кеша
 */
export const QUERY_GC_TIME = {
  /** Транзакции - храним в кеше 15 минут */
  TRANSACTIONS: 15 * 60 * 1000,
  /** Категории - храним в кеше 24 часа (редко меняются) */
  CATEGORIES: 24 * 60 * 60 * 1000,
  /** Счета - храним в кеше 30 минут */
  ACCOUNTS: 30 * 60 * 1000,
  /** Доступные месяцы и годы - храним 30 минут */
  AVAILABLE_MONTHS: 30 * 60 * 1000,
  /** Значение по умолчанию (30 минут) */
  DEFAULT: 30 * 60 * 1000,
} as const;

/**
 * Количество попыток повтора при ошибке
 */
export const QUERY_RETRY = {
  /** Для queries - 2 попытки */
  QUERIES: 2,
  /** Для mutations - 1 попытка */
  MUTATIONS: 1,
} as const;
